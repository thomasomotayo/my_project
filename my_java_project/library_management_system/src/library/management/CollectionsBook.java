    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        
        
          if(jTextFieldAccerssion.getText().length()==0){
            JOptionPane.showMessageDialog(this, "Field is empty!" );
        }
        else if(jTextFieldISBN.getText().length()==0){
            JOptionPane.showMessageDialog(this, "Field is empty!");
        }
        else if(jTextFieldCl.getText().length()==0){
            JOptionPane.showMessageDialog(this, "Field is empty!");
        }
        else if(jTextFieldTitle.getText().length()==0){
            JOptionPane.showMessageDialog(this, "Field is empty!");
        }
        else if(jTextFieldAuthorName.getText().length()==0){
            JOptionPane.showMessageDialog(this, "Field is empty!");
        }
        else if(jTextFieldEdition.getText().length()==0){
            JOptionPane.showMessageDialog(this, "Field is empty!");
        }
        else if(jTextField1Shelf.getText().length()==0){
            JOptionPane.showMessageDialog(this, "Field is empty!");
        }
        else if(jTextField1RowNo.getText().length()==0){
            JOptionPane.showMessageDialog(this, "Field is empty!");
        }
        else if(jTextField1column.getText().length()==0){
            JOptionPane.showMessageDialog(this, "Field is empty!");
        }

        else{

            try{

                con = DriverManager.getConnection("jdbc:mysql://localhost/library_managements", "root", "ethomdigital");
                String query = "insert into books (accersion_no, isbn_no, cl_no, book_title, author_name, book_edition, shelf_no, row_no, column_no) values(?,?,?,?,?,?,?,?,?)";
                pst = con.prepareStatement(query);
                pst.setString(1, jTextFieldAccerssion.getText());
                pst.setString(2, jTextFieldISBN.getText());
                pst.setString(3, jTextFieldCl.getText());
                pst.setString(4, jTextFieldTitle.getText());
                pst.setString(5, jTextFieldAuthorName.getText());
                pst.setString(6, jTextFieldEdition.getText());
                pst.setString(7, jTextField1Shelf.getText());
                pst.setString(8, jTextField1RowNo.getText());
                pst.setString(9, jTextField1column.getText());
                

                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "BOOK SUCCESSFULLY ADDED!");

                jTextFieldAccerssion.setText(null);
                jTextFieldISBN.setText(null);
                jTextFieldCl.setText(null);
                 jTextFieldTitle.setText(null);
                jTextFieldAuthorName.setText(null);
                jTextFieldEdition.setText(null);
                  jTextField1Shelf.setText(null);
                jTextFieldISBN.setText(null);
                jTextField1column.setText(null);

            }catch(Exception ex){
                JOptionPane.showMessageDialog(null, ex);

            }        // TODO add your handling code here:

            //new jPanelResetP().setVisible(true);
           
        }
    }//GEN-LAST:event_jButton4ActionPerformed


    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/library_managements", "root", "ethomdigital");

            Statement st = con.createStatement();
            String sql = "select * from books";
            ResultSet rs = st.executeQuery(sql);

            while(rs.next()){
                String accersion_no = rs.getString("accersion_no");
                String isbn_no = rs.getString("isbn_no");
                String cl_no = rs.getString("cl_no");
                String book_title = rs.getString("book_title");
                String author_name = rs.getString("author_name");
                String book_edition = rs.getString("book_edition");
                String shelf_no = rs.getString("shelf_no");
                String row_no = rs.getString("row_no");
                String column_no = rs.getString("column_no");
               

                String tbData[] = {accersion_no, isbn_no, cl_no, book_title, author_name,book_edition, shelf_no, row_no, column_no};
                DefaultTableModel tblModel = (DefaultTableModel)jTableBookList.getModel();
                tblModel.addRow(tbData);
            }
            con.close();

        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed


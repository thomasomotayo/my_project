    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
       
        
         if(TextRegNo.getText().length()==0){
            JOptionPane.showMessageDialog(this, "Please enter Registration No" );
        }
        else if(TextName.getText().length()==0){
            JOptionPane.showMessageDialog(this, "Please enter Student's Name");
        }
        else if(ComboLevel.getSelectedIndex()==0) {
            JOptionPane.showMessageDialog(this, "Please select Level");
        }
        else if(ComboSemester.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(this, "Please select Semester");
        }
        else if(TextContact.getText().length()==0){
            JOptionPane.showMessageDialog(this, "Please enter Phone Number");
        }

        else{

            try{

               
               
                con = DriverManager.getConnection("jdbc:mysql://localhost/library_managements", "root", "ethomdigital");
                String query = "insert into students (reg_no, name, level, semester, contact) values(?,?,?,?,?)";
                pst = con.prepareStatement(query);
                pst.setString(1, TextRegNo.getText());
                pst.setString(2, TextName.getText());
                pst.setString(3, ComboLevel.getSelectedItem().toString());
                pst.setString(4, ComboSemester.getSelectedItem().toString());
                pst.setString(5, TextContact.getText());
            
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "REGISTRATION SUCCESSFUL!");

                TextRegNo.setText(null);
                TextName.setText(null);
                ComboLevel.setSelectedItem(null);
                ComboSemester.setSelectedItem(null);
                TextContact.setText(null);
                
            }catch(Exception ex){
                JOptionPane.showMessageDialog(null, ex);
                
            }
        }

                  
        
        
        
        
        
        
        
        
        
        
        
    }//GEN-LAST:event_jButton4ActionPerformed


    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
         if(jTextFieldUsername.getText().isEmpty()|| jPasswordFieldPassword1.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Email or Password is empty!");
        }else{
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/library_managements", "root", "ethomdigital");

            String sql = "select * from admin where username = ? and password = ?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, jTextFieldUsername.getText());
            pst.setString(2, jPasswordFieldPassword1.getText());
            ResultSet rs = pst.executeQuery();

            if(rs.next()){
                AdminHome dashboard = new AdminHome();
                this.hide();
                dashboard.setVisible(true);
            }else{
                
                 ImageIcon icon = new ImageIcon("exist2.jpg");
                JOptionPane.showMessageDialog(null, "<html><b style =\"color: red; font-size: 20px\"> Incorrect <br> Username or Password</b></html>", "Show", JOptionPane.INFORMATION_MESSAGE, icon );
                jTextFieldUsername.setText("");
                jPasswordFieldPassword1.setText("");
                jTextFieldUsername.requestFocus();
            }

        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
         }
    }//GEN-LAST:event_jButton3ActionPerformed


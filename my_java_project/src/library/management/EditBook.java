    private void jTextFieldAccerNoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTextFieldAccerNoMouseClicked
       try{
            
        Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/library_managements", "root", "ethomdigital");

            String sql = "select * from books where accersion_no =?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, jTextFieldAccerNo.getText());
            ResultSet rs = pst.executeQuery();
            if(rs.next()){
                String book_title = rs.getString("book_title");
                jTextFieldBookTitle.setText(book_title);
                
                String a_name = rs.getString("author_name");
                jTextFieldAuthorName.setText(a_name);
                
                String b_edition = rs.getString("book_edition");
                jTextFieldEdition.setText(b_edition);
                
                String isbn = rs.getString("isbn_no");
               jTextFieldIsbn.setText(isbn);
               
               String sh_number = rs.getString("shelf_no");
               jTextFieldShelfNo.setText(sh_number);
               
               String row = rs.getString("row_no");
               jTextFieldRowNo.setText(row);
               
               String column = rs.getString("column_no");
               jTextFieldColumn.setText(column);
               
               String cl = rs.getString("column_no");
               jTextFieldCl.setText(cl);
               
       
            }
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jTextFieldAccerNoMouseClicked


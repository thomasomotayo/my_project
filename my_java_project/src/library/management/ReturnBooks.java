    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        // TODO add your handling code here:
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/library_managements", "root", "ethomdigital");

            Statement st = con.createStatement();
            String sql = "select * from returned_book";
            ResultSet rs = st.executeQuery(sql);

            while(rs.next()){
                String reg_no = rs.getString("reg_no");
                String name = rs.getString("name");
                String level = rs.getString("level");
                String semester = rs.getString("semester");
                String accerssion = rs.getString("accerssion_no");
                String date = rs.getString("date");
               

                String tbData[] = {reg_no, name, level, semester, accerssion,date};
                DefaultTableModel tblModel = (DefaultTableModel)jTableReturn.getModel();
                tblModel.addRow(tbData);
            }
            con.close();

        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton9ActionPerformed

